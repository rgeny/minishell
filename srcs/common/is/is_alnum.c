#include "is.h"

int	is_alnum(char c)
{
	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z') || c == '_')
		return (IS_ALPHA);
	else if (c >= '0' && c <= '9')
		return (IS_NUM);
	else
		return (IS_OTHER);
}
