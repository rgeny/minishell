#include "is.h"

bool	is_valid_name(char *s)
{
	int	i;

	i = 0;
	while (s[i] && s[i] != '=')
	{
		if (!((s[i] >= 'a' && s[i] <= 'z') || (s[i] >= 'A' && s[i] <= 'Z')
				|| s[i] == '_' || (i && s[i] >= '0' && s[i] <= '9')))
			return (false);
		i++;
	}
	if (!i)
		return (false);
	return (true);
}
