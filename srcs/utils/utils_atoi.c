int	uti_atoi(char *s)
{
	long	n;
	int		i;

	n = 0;
	i = 0;
	while (s[i])
	{
		n = n * 10 + s[i] - '0';
		if (n > 2147483647)
			return (0);
		i++;
	}
	return (n);
}
